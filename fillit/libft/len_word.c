#include "libft.h"

int	len_word(char const *s, char c)
{
	size_t i;
	size_t l;

	i = 0;
	l = 0;
	while (s[i] == c)
		i++;
	while (s[i] != c && s[i] != '\0')
	{
		i++;
		l++;
	}
	return (l);
}
