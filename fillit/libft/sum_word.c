#include "libft.h"

int	sum_word(char const *s, char c)
{
	size_t i;
	size_t sum;

	i = 0;
	sum = 0;
	if (s[i] == '\0')
		sum = 0;
	while (s[i])
	{
		while (s[i] == c)
			i++;
		if (s[i] != c && s[i] != '\0')
			sum++;
		while (s[i] != c && s[i] != '\0')
			i++;
	}
	return (sum);
}
