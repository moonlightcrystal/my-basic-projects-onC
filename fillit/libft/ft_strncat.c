#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	size_t	i2;

	i2 = ft_strlen(s1);
	i = 0;
	while (s2[i] && i < n)
	{
		s1[i2 + i] = s2[i];
		i++;
	}
	s1[i2 + i] = '\0';
	return (s1);
}
