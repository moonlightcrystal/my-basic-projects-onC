#include "libft.h"

char	*ptr_word(char const *s, char c)
{
	int	i;

	i = 0;
	if (!s)
		return ((char *)s + i);
	while (s)
	{
		if (s[i] != c)
			return ((char *)s + i);
		i++;
	}
	return ((char *)s);
}
