#include "libft.h"

char	*ft_point_w(char const *s, char c)
{
	int	i;

	i = 0;
	while (s[i])
	{
		if (s[i] != c)
			return ((char *)s + i);
		i++;
	}
	if (!s[i])
		return ((char *)s + i);
	return ((char *)s);
}
